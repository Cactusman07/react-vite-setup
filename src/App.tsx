import { BrowserRouter, Routes, Route } from "react-router-dom";
import { Home, NotFound } from "./components/componentIndex";
import "./App.css";

export function App() {
  return (
    <div>
      <Routes>
        <Route path="/" element={<Home />} />
        <Route path="*" element={<NotFound />} />
      </Routes>
    </div>
  );
}

export function WrappedApp() {
  return (
    <BrowserRouter>
      <App />
    </BrowserRouter>
  );
}
